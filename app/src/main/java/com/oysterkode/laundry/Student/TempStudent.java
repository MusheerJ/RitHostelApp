package com.oysterkode.laundry.Student;

public class TempStudent {
    private String h, n;

    public TempStudent() {
    }

    public TempStudent(String h, String n) {
        this.h = h;
        this.n = n;
    }

    public String getH() {
        return h;
    }

    public void setH(String h) {
        this.h = h;
    }

    public String getN() {
        return n;
    }

    public void setN(String n) {
        this.n = n;
    }
}
